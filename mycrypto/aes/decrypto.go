/**
 * 逆字节代换
 * @param state 状态矩阵
 */
func subBytesInv(state [][]byte) {
	for i := 0; i < 4; i++ {
		for j := 0; j < 4; j++ {
			state[i][j] = sBoxInv[state[i][j]]
		}
	}
}

/**
 * 逆行移位
 * @param state 状态矩阵
 */
func shiftRowsInv(state [][]byte) {
	var tempByte byte

	tempByte = state[1][3]
	state[1][3] = state[1][2]
	state[1][2] = state[1][1]
	state[1][1] = state[1][0]
	state[1][0] = tempByte

	tempByte = state[2][0]
	state[2][0] = state[2][2]
	state[2][2] = tempByte

	tempByte = state[2][1]
	state[2][1] = state[2][3]
	state[2][3] = tempByte

	tempByte = state[3][0]
	state[3][0] = state[3][1]
	state[3][1] = state[3][2]
	state[3][2] = state[3][3]
	state[3][3] = tempByte
}

func multiply(x byte, y byte) byte {
	return ((y & 0x01) * x) ^
		((y >> 1 & 0x01) * xtime(x)) ^
		((y >> 2 & 0x01) * xtime(xtime(x))) ^
		((y >> 3 & 0x01) * xtime(xtime(xtime(x)))) ^
		((y >> 4 & 0x01) * xtime(xtime(xtime(xtime(x)))))
}

/**
 * 逆列混淆
 * @param state 状态矩阵
 */
func mixColumnsInv(state [][]byte) {
	var d0, d1, d2, d3 byte
	for i := 0; i < 4; i++ {

		d0 = state[0][i]
		d1 = state[1][i]
		d2 = state[2][i]
		d3 = state[3][i]

		state[0][i] = multiply(d0, 0x0e) ^ multiply(d1, 0x0b) ^
			multiply(d2, 0x0d) ^ multiply(d3, 0x09)
		state[1][i] = multiply(d0, 0x09) ^ multiply(d1, 0x0e) ^
			multiply(d2, 0x0b) ^ multiply(d3, 0x0d)
		state[2][i] = multiply(d0, 0x0d) ^ multiply(d1, 0x09) ^
			multiply(d2, 0x0e) ^ multiply(d3, 0x0b)
		state[3][i] = multiply(d0, 0x0b) ^ multiply(d1, 0x0d) ^
			multiply(d2, 0x09) ^ multiply(d3, 0x0e)
	}
}

/**
 * 解密一组16字节的block
 * @param in        一组16字节的block
 * @param roundKey  密钥流
 * @param nr        解密轮数，根据密钥的长度而变化
 * @param out       明文字节流
 */
func decryptoBlock(in []byte, roundKey []byte, nr int, out []byte) {
	var round int

	state := make([][]byte, 4)
	for i := range state {
		state[i] = make([]byte, 4)
	}

	for i := 0; i < 4; i++ {
		for j := 0; j < 4; j++ {
			state[j][i] = in[i*4+j]
		}
	}

	addRoundKey(nr, state, roundKey)

	for round = nr - 1; round > 0; round-- {
		shiftRowsInv(state)
		subBytesInv(state)
		addRoundKey(round, state, roundKey)
		mixColumnsInv(state)
	}

	shiftRowsInv(state)
	subBytesInv(state)
	addRoundKey(0, state, roundKey)

	for i := 0; i < 4; i++ {
		for j := 0; j < 4; j++ {
			out[i*4+j] = state[j][i]
		}
	}
}

/**
 * 对外接口，将密文分组并依次调用decryptoBlock
 * @param cipher    密文
 * @param key       密钥
 */
func Decrypto(cipher []byte, key []byte) []byte {

	ret := make([]byte, 0)
	roundKey := make([]byte, 240)
	keySizeInBit := len(key) * 8
	nbK := keySizeInBit / 32
	nr := nbK + 6

	keyExpansion(key, roundKey, nbK, nr)

	out := make([]byte, 16)

	for i := 0; i < len(cipher); i += 16 {
		decryptoBlock(cipher[i:i+16], roundKey, nr, out)
		for i := 0; i < 16; i++ {
			ret = append(ret, out[i])
		}
	}
	return ret
}
